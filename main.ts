// Function to check if all items in a list are all 1 or 2
// 
// If it is then either player 1 or player 2 wins respectively
function rowWinChecker2 (array: number[]) {
    // Variable acts as list element counter
    index = 0
    // For each item in the input list it will check if all the items are the list are the same and non-zero
    for (let value of array) {
        if (array[index] == array[0]) {
            index += 1
            if (index == 3) {
                // Player 1 win sequence
                if (array[0] == 1) {
                    game.setGameOverMessage(true, "PLAYER 1 WINS")
                    pause(500)
                    game.gameOver(true)
                }
                // Player 2 win sequence
                if (array[0] == 2) {
                    game.setGameOverMessage(true, "PLAYER 2 WINS")
                    pause(500)
                    game.gameOver(true)
                }
            }
        } else {
            break;
        }
    }
    if (tile1 > 0 && tile2 > 0 && (tile3 > 0 && tile4 > 0) && (tile5 > 0 && tile6 > 0 && (tile7 > 0 && tile8 > 0)) && tile8 > 0) {
        // Tie sequence
        game.setGameOverMessage(false, "TIE")
        pause(500)
        game.gameOver(false)
    }
}
